// ======================
// Opposite number
// Very simple, given a number, find its opposite.

// Examples:

// 1: -1
// 14: -14
// -34: 34
export class Kata {
  static opposite(n: number) {
    return n * -1;
  }
}

// ======================
// Multiply

function multiply(a: number, b: number): number {
  return a * b;
}
